"use strict";
var num1 = 10;
var num2 = 2;
var res = 0;
res = num1 + num2;
console.log("加法的结果是：" + res);
res = num1 - num2;
console.log("减法的结果是：" + res);
res = num1 * num2;
console.log("乘法的结果是：" + res);
res = num1 / num2;
console.log("除法的结果是：" + res);
res = num1 % num2; //取余数
console.log("取模的结果是：" + res);
num1++;
console.log("num1自增后的结果是：" + num1);
num2--;
console.log("num2自减后的结果是：" + num2);
